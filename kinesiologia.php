    <p class="card-text">La kinesiología, conocida también bajo el nombre de quinesiología, es una ciencia que adopta su nombre de la conjunción de dos palabras de origen griego: por un lado, «kinesis», que significa «movimiento», y «logos», que significa «tratado». Por esta razón, podemos definir a la kinesiología como la ciencia que estudia el movimiento humano, valiéndose de la utilización de principios generales que rigen a las ciencias físicas; por lo que se la reconoce como un estudio multidisciplinario. a función de la kinesiología es comprender y restaurar la salud, en un principio, del sistema neuromuscular y, en segunda instancia, de garantizar el correcto desarrollo del cuerpo humano en su totalidad, atendiendo a las afecciones que no sólo atacan a los aparatos muscular y esquelético. De este modo, puede decirse que el objetivo de un médico especialista en esta disciplina es el de mejorar la salud, garantizar la capacidad fisiológica del paciente y prevenir las alteraciones o enfermedades. Emplearemos técnicas especiales para recobrar la salud de los individuos afectados por dolencias musculares. Las mismas consisten en la prescripción de movimientos físicos específicos a nivel cualitativo y cuantitativo, ya que los kinesiólogos conocen las patologías vinculadas al mal funcionamiento de cada músculo, por lo que se encuentran habilitados para recetar ejercicios preventivos y asistenciales para mejorar y fortalecer al organismo en su totalidad.</p>
